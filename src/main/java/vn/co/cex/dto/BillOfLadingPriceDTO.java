package vn.co.cex.dto;

public class BillOfLadingPriceDTO extends BaseDTO {
	private Integer goodsTypeId;
	private String goodsTypeName;
	private Integer numberOfBill;
	private Float totalPrice;
	private Float totalDistance;
	private Float totalWeight;

	public Integer getGoodsTypeId() {
		return goodsTypeId;
	}

	public String getGoodsTypeName() {
		return goodsTypeName;
	}

	public Integer getNumberOfBill() {
		return numberOfBill;
	}

	public Float getTotalPrice() {
		return totalPrice;
	}

	public Float getTotalDistance() {
		return totalDistance;
	}

	public Float getTotalWeight() {
		return totalWeight;
	}

	public void setGoodsTypeId(Integer goodsTypeId) {
		this.goodsTypeId = goodsTypeId;
	}

	public void setGoodsTypeName(String goodsTypeName) {
		this.goodsTypeName = goodsTypeName;
	}

	public void setNumberOfBill(Integer numberOfBill) {
		this.numberOfBill = numberOfBill;
	}

	public void setTotalPrice(Float totalPrice) {
		this.totalPrice = totalPrice;
	}

	public void setTotalDistance(Float totalDistance) {
		this.totalDistance = totalDistance;
	}

	public void setTotalWeight(Float totalWeight) {
		this.totalWeight = totalWeight;
	}
}
