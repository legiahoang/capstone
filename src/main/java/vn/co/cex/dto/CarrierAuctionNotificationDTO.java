package vn.co.cex.dto;

public class CarrierAuctionNotificationDTO extends BaseDTO {

	private int carrierAuctionId;
	private int billOfLadingId;
	private int status;
	private int userId;
	private String username;
	private String email;

	public int getCarrierAuctionId() {
		return carrierAuctionId;
	}

	public void setCarrierAuctionId(int carrierAuctionId) {
		this.carrierAuctionId = carrierAuctionId;
	}

	public int getBillOfLadingId() {
		return billOfLadingId;
	}

	public void setBillOfLadingId(int billOfLadingId) {
		this.billOfLadingId = billOfLadingId;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
