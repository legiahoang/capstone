package vn.co.cex.utils.mail;

import java.util.HashMap;

public class TNMailMessage {
	private String subject = "";
    private String content = "";
    private String signature;
    private String contentType = "text/plain";
    private String from;
    private String[] toList;
    private String[] toPersonalList;
    private String fromPersonal;
    private String[] bccList;
    private String[] ccList;
	private HashMap<String, String> attachedFiles;
	public TNMailMessage() {
	}
	/**
	 * @return the subject
	 */
	public String getSubject() {
		return subject;
	}
	/**
	 * @param subject the subject to set
	 */
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the signature
	 */
	public String getSignature() {
		return signature;
	}
	/**
	 * @param signature the signature to set
	 */
	public void setSignature(String signature) {
		this.signature = signature;
	}
	/**
	 * @return the contentType
	 */
	public String getContentType() {
		return contentType;
	}
	/**
	 * @param contentType the contentType to set
	 */
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	/**
	 * @return the from
	 */
	public String getFrom() {
		return from;
	}
	/**
	 * @param from the from to set
	 */
	public void setFrom(String from) {
		this.from = from;
	}
	/**
	 * @return the toList
	 */
	public String[] getToList() {
		return toList;
	}
	/**
	 * @param toList the toList to set
	 */
	public void setToList(String[] toList) {
		this.toList = toList;
	}
	/**
	 * @return the toPersonalList
	 */
	public String[] getToPersonalList() {
		return toPersonalList;
	}
	/**
	 * @param toPersonalList the toPersonalList to set
	 */
	public void setToPersonalList(String[] toPersonalList) {
		this.toPersonalList = toPersonalList;
	}
	/**
	 * @return the fromPersonal
	 */
	public String getFromPersonal() {
		return fromPersonal;
	}
	/**
	 * @param fromPersonal the fromPersonal to set
	 */
	public void setFromPersonal(String fromPersonal) {
		this.fromPersonal = fromPersonal;
	}
	/**
	 * @return the bccList
	 */
	public String[] getBccList() {
		return bccList;
	}
	/**
	 * @param bccList the bccList to set
	 */
	public void setBccList(String[] bccList) {
		this.bccList = bccList;
	}
	/**
	 * @return the ccList
	 */
	public String[] getCcList() {
		return ccList;
	}
	/**
	 * @param ccList the ccList to set
	 */
	public void setCcList(String[] ccList) {
		this.ccList = ccList;
	}
	/**
	 * @return the attachedFiles
	 */
	public HashMap<String, String> getAttachedFiles() {
		return attachedFiles;
	}
	/**
	 * @param attachedFiles the attachedFiles to set
	 */
	public void setAttachedFiles(HashMap<String, String> attachedFiles) {
		this.attachedFiles = attachedFiles;
	}

}
